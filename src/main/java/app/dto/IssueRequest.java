package app.dto;

import app.model.IssuePriority;
import app.model.IssueStatus;
import app.model.IssueType;

import java.util.ArrayList;
import java.util.List;

public class IssueRequest {
    private String title;  // Required
    private String description;  // Required, Base64 encoded string for binary data
    private IssuePriority priority = IssuePriority.MEDIUM;
    private IssueType type = IssueType.TASK;
    private IssueStatus status = IssueStatus.OPEN;
    private String resolution = "";
    private Long reporterId = null;
    private Long assigneeId = null;
    private Long teamId = null;
    private List<String> tags = new ArrayList<>();
    private List<String> attachments = new ArrayList<>();

    // Getters and Setters
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public IssuePriority getPriority() {
        return priority;
    }

    public void setPriority(IssuePriority priority) {
        this.priority = priority;
    }

    public IssueType getType() {
        return type;
    }

    public void setType(IssueType type) {
        this.type = type;
    }

    public IssueStatus getStatus() {
        return status;
    }

    public void setStatus(IssueStatus status) {
        this.status = status;
    }

    public String getResolution() {
        return resolution;
    }

    public void setResolution(String resolution) {
        this.resolution = resolution;
    }

    public Long getReporterId() {
        return reporterId;
    }

    public void setReporterId(Long reporterId) {
        this.reporterId = reporterId;
    }


    public Long getAssigneeId() {
        return assigneeId;
    }

    public void setAssigneeId(Long assigneeId) {
        this.assigneeId = assigneeId;
    }

    public Long getTeamId() {
        return teamId;
    }

    public void setTeamId(Long teamId) {
        this.teamId = teamId;
    }

    public List<String> getTags() {
        return tags;
    }

    public void setTags(List<String> tags) {
        this.tags = tags;
    }

    public List<String> getAttachments() {
        return attachments;
    }

    public void setAttachments(List<String> attachments) {
        this.attachments = attachments;
    }
}
